package it.crem.db.conf;

import java.util.Set;

import it.crem.db.BaseObject;

public class GruppiFoto extends BaseObject implements java.io.Serializable{

	private static final long serialVersionUID = 1L;
	private String nome = null;
	private String icona = null;
	private String url = null;
	private String cartella = null;
	private String path = null;
	private String webpath = null;
	private String album = null;
	private String anno = null;
	private Integer numero = 0;
	private String iconaPath = null;
	private Set<Foto> fotos = null;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getIcona() {
		return icona;
	}
	public void setIcona(String icona) {
		this.icona = icona;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getCartella() {
		return cartella;
	}
	public void setCartella(String cartella) {
		this.cartella = cartella;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getWebpath() {
		return webpath;
	}
	public void setWebpath(String webpath) {
		this.webpath = webpath;
	}
	
	
	public String getAnno() {
		return anno;
	}
	public void setAnno(String anno) {
		this.anno = anno;
	}
	public String getAlbum() {
		return album;
	}
	public void setAlbum(String album) {
		this.album = album;
	}
	public Integer getNumero() {
		return numero;
	}
	public void setNumero(Integer numero) {
		this.numero = numero;
	}
	public String getIconaPath() {
		return iconaPath;
	}
	public void setIconaPath(String iconaPath) {
		this.iconaPath = iconaPath;
	}
	public Set<Foto> getFotos() {
		return fotos;
	}
	public void setFotos(Set<Foto> fotos) {
		this.fotos = fotos;
	}
	
	
	
}
